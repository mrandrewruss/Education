seqNum = int(input('Введите последовательность чисел: '))
count = 0

for point in range(1, seqNum + 1):
  print('Введите', point, '-е число: ', end = ' ')
  num = int(input())
  count += 1
  for step in range(2, num // 2 + 1):
    if num % step == 0:
      count -= 1
      break
print('Из введенных', count, 'чисел - простых: ', count)

