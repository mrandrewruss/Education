# Напишите программу,
# которая запрашивает у пользователя число N
# и находит сумму факториалов 1! + 2! + 3! +... + N! 

number = int(input('Введите число N: '))
summ = 0
factorial = 1

for num in range(1, number + 1):
  factorial *= num
  summ += factorial
print('Сумма факториалов с 1 до ', number, '=', summ)

